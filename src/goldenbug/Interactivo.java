    public void sustituir(){
        String sustituido=this.textoCifrado.getText().toLowerCase();
        for(CustomTextfield cT:letras){
            cT.updateMap();
        }
        Iterator<Map.Entry<Character,Character>> iterator =this.tableMap.entrySet().iterator();
        while(iterator.hasNext()){
            Entry<Character,Character> entry=iterator.next();
            sustituido=sustituido.replace(entry.getKey(),entry.getValue());
        }
        this.textoDescifrado.setText(sustituido);
    }
