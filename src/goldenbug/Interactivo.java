    private void crearTableroMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_crearTableroMouseReleased
        this.correspondencias.removeAll();
        Map<Character,Character> mapTable= new HashMap();
        String textoCifrado=this.textoCifrado.getText().toLowerCase().replaceAll("[^A-Za-z]+", "");
        GridBagConstraints cons= new GridBagConstraints();
        cons.gridx=0;
        cons.gridy=0;
        cons.weightx=1;
        cons.weighty=1;
        for(int i=0;i<textoCifrado.length();i++){
            char c=textoCifrado.charAt(i);
            if(!mapTable.containsKey(c)){
                mapTable.put(c,'*');
                JTextField letra=new JTextField();
                CustomTextfield traduccion=new CustomTextfield(letra,mapTable);
                letras.add(traduccion);
                traduccion.setPreferredSize(new Dimension(25,25));
                letra.setEditable(false);
                letra.setPreferredSize(new Dimension(25,25));
                letra.setText(""+c);
                
                this.correspondencias.add(letra,cons);
                cons.gridx++;
                this.correspondencias.add(traduccion,cons);
                cons.gridy++;
                cons.gridx=0;
            }
        }
       
        this.correspondencias.repaint();
        this.correspondencias.revalidate();
        this.tableMap=mapTable;
         sustituir();
        
    }//GEN-LAST:event_crearTableroMouseReleased

